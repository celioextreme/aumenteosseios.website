   $('#Button1').addClass('visibility-hidden');
   function Button1Scroll()
   {
      var $obj = $("#Button1");
      if (!$obj.hasClass("in-viewport") && $obj.inViewPort(false))
      {
         $obj.addClass("in-viewport");
         AnimateCss('Button1', 'transform-pulse', 500, 750);
      }
      else
      if ($obj.hasClass("in-viewport") && !$obj.inViewPort(true))
      {
         $obj.removeClass("in-viewport");
         AnimateCss('Button1', 'animate-fade-out', 500, 0);
      }
   }
   if (!$('#Button1').inViewPort(true))
   {
      $('#Button1').addClass("in-viewport");
   }
   Button1Scroll();
   $(window).scroll(function(event)
   {
      Button1Scroll();
   });
